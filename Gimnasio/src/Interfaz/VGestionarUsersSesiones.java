    public void buscarUsuariosDisponibles() {
        
        this.mtu1 = (ModeloTablaUsuarios) tablaUsuarios1.getModel();
        try {
            if (!textoDNI1.getText().isEmpty()) {
                mtu1.setFilas(fa.consultarPosiblesUsuarios(Integer.parseInt(textoDNI1.getText(), sesion.getIdSesion())));
            } else {
                java.util.List<Usuario> u = fa.consultarPosiblesUsuarios(sesion.getIdSesion());
                mtu1.setFilas(u);
            }
            
        } catch (Exception e) {
            this.fa.muestraExcepcion("Ooops, algo ha salido mal en la búsqueda.");
        }
        if (mtu1.getRowCount() > 0) {
            tablaUsuarios1.setRowSelectionInterval(0, 0);
        }
        
    }
    
    public void buscarUsuariosDeEsaSesion() {
        
        this.mtu3 = (ModeloTablaUsuarios) tablaUsuarios3.getModel();
        java.util.List<Usuario> users;
        try {
            if (!textoDNI3.getText().isEmpty()) {
                users = fa.consultarUsuariosdeUnaSesion(textoDNI3.getText(), sesion.getIdSesion());
                if (!users.isEmpty()) {
                    mtu3.setFilas(users);
                } else {
                    fa.muestraExcepcion("Aun no hay usuarios/as en esta sesión.");
                }
            } else {
                users = fa.consultarUsuariosdeUnaSesion(sesion.getIdSesion());
                if (!users.isEmpty()) {
                    mtu3.setFilas(users);
                } else {
                    fa.muestraExcepcion("Aun no hay usuarios/as en esta sesión.");
                }
            }
            
        } catch (Exception e) {
            this.fa.muestraExcepcion("Ooops, algo ha salido mal en la búsqueda.");
        }
        if (mtu1.getRowCount() > 0) {
            tablaUsuarios3.setRowSelectionInterval(0, 0);
        }
        
    }
    
    public boolean checkFields() {
        if (!textoDNI2.getText().isEmpty() && !textoEmail.getText().isEmpty()
                && !textoNombre.getText().isEmpty() && !textoTelefono.getText().isEmpty()) {
            return true;
        }
        return false;
    }

    //Actualizar la tabla de usuarios
    public void actualizarTabla1() {
        buscarUsuariosDisponibles();
        this.tablaUsuarios1.updateUI();
    }

    //Actualizar la tabla de usuarios
    public void actualizarTabla3() {
        buscarUsuariosDeEsaSesion();
        this.tablaUsuarios3.updateUI();
    }
    
    public boolean hayPlazas() {
        int numUsuarios = fa.consultarUsuariosdeUnaSesion(sesion.getIdSesion()).size();
        int numPlazas = sesion.getPlazas();
        if (numUsuarios == numPlazas) {
            return false;
        } else {
            return true;
        }
    }

