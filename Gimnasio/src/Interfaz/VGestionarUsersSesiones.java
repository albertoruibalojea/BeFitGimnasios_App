    private void btnAdd1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdd1ActionPerformed
        if (fa.visualizarUsuario(usuario.getDni_usuario()) != null) {
            if (!this.hayPlazas()) {
                fa.muestraExcepcion("No hay plazas en esta sesion.");
            } else {
                fa.addUserToSesion(this.usuario, this.sesion.getIdSesion(), this.establecimiento);
                this.actualizarTabla1();
            }
        } else {
            fa.muestraExcepcion("El usuario (ya) no pertenece a nuestros gimnasios. Por favor, lo tiene que dar de alta en la columna de su derecha.");
        }

    }//GEN-LAST:event_btnAdd1ActionPerformed

    private void btnAdd2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdd2ActionPerformed
        if (this.checkFields()) {
            if (fa.visualizarUsuario(textoDNI2.getText()) == null) {
                this.usuario = new Usuario(textoDNI2.getText(), textoNombre.getText(), textoEmail.getText(), textoTelefono.getText(), checkBoxSocioa.isSelected());
                fa.insertarUsuario(usuario);
                if (!this.hayPlazas()) {
                    fa.muestraExcepcion("No hay plazas en esta sesión.");
                } else {
                    fa.addUserToSesion(this.usuario, this.sesion.getIdSesion(), this.establecimiento);
                    this.actualizarTabla1();
                }
            } else {
                fa.muestraExcepcion("Este usuario ya pertenece a nuestros gimnasios. Por favor, búsquelo en la columna de su izquierda.");
            }
        } else {
            fa.muestraExcepcion("Es necesario cubrir todos los campos.");
        }
    }//GEN-LAST:event_btnAdd2ActionPerformed

