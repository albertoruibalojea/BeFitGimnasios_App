    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        if (!this.numEjemplares.getText().isEmpty()) {
            int cuantos = Integer.parseInt(numEjemplares.getText());

            Ejemplar ej = new Ejemplar(equipamiento, campoDescripcion.getText(), establecimiento);
            for (int i = 0; i < cuantos; i++) {
                fa.insertarEjemplar(equipamiento.getId(), ej);
            }
            this.actualizarTabla();
        }
    }//GEN-LAST:event_btnAddActionPerformed

