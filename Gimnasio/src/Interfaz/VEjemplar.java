    public void buscarEjemplares() {
        ModeloTablaEjemplares mte;

        mte = (ModeloTablaEjemplares) tablaEjemplares.getModel();

        try {
            if (!campoIDEquip.getText().isEmpty()) {
                this.equipamiento = fa.consultarEquipamiento(Integer.parseInt(campoIDEquip.getText()));
                if (!campoIDEjemplar.getText().isEmpty()) {
                    mte.setFilas(fa.obtenerEjemplares(Integer.parseInt(campoIDEjemplar.getText()), establecimiento, equipamiento));
                } else {
                    mte.setFilas(fa.obtenerEjemplares(establecimiento, equipamiento));
                }
                if(fa.obtenerEjemplares(establecimiento, equipamiento).isEmpty()){
                    fa.muestraExcepcion("No existen ejemplares. Añádelos en la parte inferior de la ventana.");
                }
            } else fa.muestraExcepcion("Es necesario introducir al menos el ID del equipamiento.");
        } catch (Exception e) {
            this.fa.muestraExcepcion("Ooops, algo ha salido mal en la búsqueda.");
        }

        if (mte.getRowCount() > 0) {
            tablaEjemplares.setRowSelectionInterval(0, 0);
        }
    }

    private Establecimiento getEstablecimientoActual() {
        return fa.getEstablecimientoActual();
    }

    public void actualizarTabla() {

        this.buscarEjemplares();
        this.mte = (ModeloTablaEjemplares) tablaEjemplares.getModel();

        id_ejemplar = this.mte.obtenerEjemplar(tablaEjemplares.getSelectedRow()).getId_ejemplar();

        Ejemplar e = fa.visualizarEjemplar(id_ejemplar, equipamiento, establecimiento);

        campoAnoCompra.setText(e.getAno_compra().toString());
        campoDescripcion.setText(e.getDescripcion());

        this.tablaEjemplares.updateUI();
    }

