    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        if (this.checkFields()) {
            this.usuario = new Usuario(textoDNI.getText(), textoNombre.getText(), textoEmail.getText(), textoTelefono.getText(), checkBoxSocioa.isSelected());
            
            fa.actualizarUsuario(this.usuario);
            this.actualizarTabla();
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        buscarUsuarios();
        
        this.modUsuarios = (ModeloTablaUsuarios) tablaUsuarios.getModel();
        dniUsuario = this.modUsuarios.obtenerUsuario(tablaUsuarios.getSelectedRow()).getDni_usuario();
        
        this.usuario = fa.visualizarUsuario(dniUsuario);
        this.setFields(usuario);
        
        btnLimpiar.setEnabled(true);

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUsuariosMouseClicked
        
        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        this.dniUsuario = source.getModel().getValueAt(row, 0) + "";
        
        this.usuario = fa.visualizarUsuario(dniUsuario);
        this.setFields(usuario);

    }//GEN-LAST:event_tablaUsuariosMouseClicked

    private void btnBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaActionPerformed
        
        if (this.checkFields()) {
            fa.borrarUsuario(this.usuario);
            this.actualizarTabla();
            this.clearFields();
            
            btnLimpiar.setEnabled(false);
        }
    }//GEN-LAST:event_btnBajaActionPerformed

