    //Actualizar la tabla de usuarios
    public void actualizarTabla() {
        buscarUsuarios();
        this.modUsuarios = (ModeloTablaUsuarios) tablaUsuarios.getModel();
        dniUsuario = this.modUsuarios.obtenerUsuario(tablaUsuarios.getSelectedRow()).getDni_usuario();
        
        this.usuario = fa.visualizarUsuario(dniUsuario);
        this.setFields(usuario);
        
        this.tablaUsuarios.updateUI();
    }
    
    public void clearFields() {
        textoDNI.setText(null);
        textoEmail.setText(null);
        textoNombre.setText(null);
        textoTelefono.setText(null);
        checkBoxSocioa.setSelected(false);
        textoDNI.setEditable(true);
        btnSesiones.setEnabled(false);
    }
    
    public void setFields(Usuario u) {
        textoDNI.setText(u.getDni_usuario());
        textoEmail.setText(u.getEmail());
        textoNombre.setText(u.getNombre());
        textoTelefono.setText(u.getTelefono());
        checkBoxSocioa.setSelected(u.isSocio());
        
        btnLimpiar.setEnabled(true);
        btnGuardar.setEnabled(true);
        btnSesiones.setEnabled(true);
        btnBaja.setEnabled(true);
        textoDNI.setEditable(false);
        
        this.consultarEstablecimientos();
    }

    //Comprueba si los campos están vacíos o no
    public boolean checkFields() {
        if (!textoDNI.getText().isEmpty() && !textoEmail.getText().isEmpty()
                && !textoNombre.getText().isEmpty() && !textoTelefono.getText().isEmpty()) {
            if (textoDNI.getText().length() == 9) {
                if (textoTelefono.getText().length() == 9) {
                    try {
                        Integer num = Integer.parseInt(textoTelefono.getText());
                    } catch (Exception e) {
                        fa.muestraExcepcion("Ups, hay algún campo incorrecto.");
                    }
                } else fa.muestraExcepcion("Ups, hay algún campo incorrecto.");
            } else fa.muestraExcepcion("Ups, hay algún campo incorrecto.");
            
        }
        return false;
    }
    
