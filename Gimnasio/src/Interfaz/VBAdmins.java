    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        this.buscarAdmins();

        this.modAdmins = (ModeloTablaAdmins) tablaAdmins.getModel();
        dniAdmin = this.modAdmins.obtenerAdmin(tablaAdmins.getSelectedRow()).getDni();

        this.admin = fa.visualizarAdmin(dniAdmin);
        this.setFields(admin);

        btnLimpiar.setEnabled(true);

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (this.checkFields()) {
            this.admin = new Administrador(campoPass.getText(), campoDNI.getText(), 
                    campoNombre.getText(), campoTelefono.getText(), campoEmail.getText(), 
                    Integer.parseInt(campoGym.getText()), Calendar.getInstance().get(Calendar.YEAR));

            fa.actualizarAdmin(this.admin);
            this.actualizarTabla();
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tablaAdminsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaAdminsMouseClicked

        JTable source = (JTable) evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        this.dniAdmin = source.getModel().getValueAt(row, 1) + "";

        this.admin = fa.visualizarAdmin(dniAdmin);
        this.setFields(admin);

    }//GEN-LAST:event_tablaAdminsMouseClicked

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed

        if (this.checkFields()) {
            this.fa.muestraExcepcion("¿Estás seguro/a de borrarlo?");
            boolean a = this.fa.comprobacionBoss("¿DESPEDIR ADMIN?.");
            if (a) {
                fa.borrarAdmin(this.admin);
                this.actualizarTabla();
                this.clearFields();

                btnLimpiar.setEnabled(false);
            } else {
                this.fa.muestraExcepcion("No puede cerrar el gimnasio.");
            }

        }

    }//GEN-LAST:event_btnBorrarActionPerformed

