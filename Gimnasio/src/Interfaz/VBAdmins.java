    public void buscarAdmins() {

        this.modAdmins = (ModeloTablaAdmins) tablaAdmins.getModel();

        if (!campoIDGym.getText().isEmpty()) {
            modAdmins.setFilas(fa.obtenerAdmins(Integer.parseInt(campoIDGym.getText()), campoDNIAdmin.getText()));
        } else {
            modAdmins.setFilas(fa.obtenerAdmins());
        }

        if (modAdmins.getRowCount() > 0) {
            tablaAdmins.setRowSelectionInterval(0, 0);
        }

    }

    //Actualizar la tabla de usuarios
    public void actualizarTabla() {
        buscarAdmins();
        this.modAdmins = (ModeloTablaAdmins) tablaAdmins.getModel();
        dniAdmin = this.modAdmins.obtenerAdmin(tablaAdmins.getSelectedRow()).getDni();

        this.admin = fa.visualizarAdmin(dniAdmin);
        this.setFields(admin);

        this.tablaAdmins.updateUI();
    }

    public void setFields(Administrador a) {
        campoDNI.setText(a.getDni());
        campoNombre.setText(a.getNombre());
        campoEmail.setText(a.getEmail());
        campoTelefono.setText(a.getTelefono());
        campoPass.setText(a.getContrasena());
        campoGym.setText(a.getEstablecimientoString());

        campoDNI.setEditable(false);
        btnBorrar.setEnabled(true);

    }

    public Boolean checkFields() {
        if (!campoDNI.getText().isEmpty() && !campoNombre.getText().isEmpty()
                && !campoEmail.getText().isEmpty() && !campoTelefono.getText().isEmpty()
                && !campoPass.getText().isEmpty() && !campoGym.getText().isEmpty()) {
            return true;
        } else {
            return false;
        }
    }

    public void clearFields() {
        campoTelefono.setText(null);
        campoEmail.setText(null);
        campoNombre.setText(null);
        campoDNI.setText(null);
        campoPass.setText(null);
        campoGym.setText(null);

        campoDNI.setEditable(true);
        btnBorrar.setEnabled(false);
    }

